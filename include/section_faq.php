		<h3 class="bloc_title_four">Que fait exactement Edgar ?</h3>
		
		<div class="bloc bloc_four">
		<p>Votre Edgar est votre majordome personnel. Son rôle est de vous faciliter la vie de chaque jour pour que vous puissiez profiter davantage de vos amis, de votre famille en vous soustrayant aux contraintes du quotidien. Il gère donc  l'ensemble de vos tâches et corvées : </p>

		<p><strong>Formule Horizon</strong> : Votre Edgar vous rend visite 1 fois par semaine pour faire le lit, faire la vaisselle, ranger et nettoyer  ou encore sortir les poubelles. Attention, Edgar n'est pas là pour nettoyer votre appartement après une soirée épique entre amis, ni pour déboucher vos toilettes.</p>

		<p><strong>Formule Zénith</strong> : Edgar intervient comme pour la formule Horizon, et se chargera également de vos courses, de passer au pressing, à la pharmacie ou chez le cordonnier, d'aller à la Poste, etc. En somme, Edgar prend en charge le traitement de vos démarches et corvées du quotidien. </p>

		<p><strong>Formule Galaxie</strong> : Edgar intervient comme dans la formule Zénith, mais 2 fois par semaine. Votre linge déposé au pressing vous sera donc restitué la même semaine.</p>
